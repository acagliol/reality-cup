#!/usr/bin/env node
/**
 * Generate split SQL files under supabase/seed-parts/ for Supabase MCP batch apply.
 *
 * Usage: node scripts/generate-category-seed-sql.mjs
 */

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import {
  GAME_CATEGORIES,
  LEGACY_CATEGORY_IDS,
  TRUTH_FAKE,
  TRUTH_REAL,
} from '../data/category-pools/config.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const MANIFEST_PATH = join(__dirname, '..', 'data', 'category-pools', 'manifest.json');
const OUT_DIR = join(__dirname, '..', 'supabase', 'seed-parts');
const COMBINED_PATH = join(__dirname, '..', 'supabase', 'seed-category-pools.sql');

function sqlEscape(value) {
  return String(value).replace(/'/g, "''");
}

function loadManifest() {
  if (!existsSync(MANIFEST_PATH)) {
    throw new Error(`Missing ${MANIFEST_PATH} — run: node scripts/build-category-pools.mjs`);
  }
  return JSON.parse(readFileSync(MANIFEST_PATH, 'utf8'));
}

function categorySql(category, pool) {
  const lines = [
    `delete from public.ai_answers where round_content_id in (`,
    `  select id from public.round_content where category_id = '${category.id}'`,
    `);`,
    `delete from public.crowd_stats where round_content_id in (`,
    `  select id from public.round_content where category_id = '${category.id}'`,
    `);`,
    `delete from public.round_content where category_id = '${category.id}';`,
    ``,
    `insert into public.round_content (id, category_id, image_url, truth_value, sort_order, active) values`,
  ];

  const rowValues = [];
  let sortOrder = 1;

  pool.real.forEach((entry, index) => {
    const id = `${category.id}-real-${String(index + 1).padStart(3, '0')}`;
    rowValues.push(
      `  ('${id}', '${category.id}', '${sqlEscape(entry.url)}', ${TRUTH_REAL}, ${sortOrder++}, true)`,
    );
  });

  pool.fake.forEach((entry, index) => {
    const id = `${category.id}-fake-${String(index + 1).padStart(3, '0')}`;
    rowValues.push(
      `  ('${id}', '${category.id}', '${sqlEscape(entry.url)}', ${TRUTH_FAKE}, ${sortOrder++}, true)`,
    );
  });

  lines.push(rowValues.join(',\n'));
  lines.push(';');
  return lines.join('\n');
}

function main() {
  const manifest = loadManifest();
  mkdirSync(OUT_DIR, { recursive: true });

  const header = [
    '-- Auto-generated by scripts/generate-category-seed-sql.mjs',
    `-- Generated from manifest: ${manifest.generatedAt}`,
    '',
    'update public.categories set active = false',
    `where id in (${LEGACY_CATEGORY_IDS.map((id) => `'${id}'`).join(', ')});`,
    '',
    'insert into public.categories (id, name, description, icon, sort_order, active) values',
    ...GAME_CATEGORIES.map(
      (c, index) =>
        `  ('${c.id}', '${sqlEscape(c.name)}', '${sqlEscape(c.description)}', '${c.icon}', ${c.sortOrder}, true)${index === GAME_CATEGORIES.length - 1 ? '' : ','}`,
    ),
    'on conflict (id) do update set',
    '  name = excluded.name,',
    '  description = excluded.description,',
    '  icon = excluded.icon,',
    '  sort_order = excluded.sort_order,',
    '  active = excluded.active;',
    '',
  ].join('\n');

  writeFileSync(join(OUT_DIR, '00-categories.sql'), `${header}\n`, 'utf8');

  const combined = [header];
  for (const category of GAME_CATEGORIES) {
    const pool = manifest.categories[category.id];
    const sql = categorySql(category, pool);
    writeFileSync(join(OUT_DIR, `${category.id}.sql`), `${sql}\n`, 'utf8');
    combined.push(sql, '');
  }

  writeFileSync(COMBINED_PATH, `${combined.join('\n')}\n`, 'utf8');
  console.log(`Wrote ${OUT_DIR} (${GAME_CATEGORIES.length + 1} files, 500 round_content rows)`);
}

main();
